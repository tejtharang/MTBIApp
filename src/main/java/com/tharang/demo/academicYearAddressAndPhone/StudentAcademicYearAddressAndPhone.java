package com.tharang.demo.academicYearAddressAndPhone;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class StudentAcademicYearAddressAndPhone {
    @Id
    private String userId;
    private String country;
    private String addressLine1;
    private String addressLine2;
    private String city;
    private String state;
    private String zip;
    private String mailReceivable;
    private String homePhone;
    private String mobilePhone;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getAddressLine1() {
        return addressLine1;
    }

    public void setAddressLine1(String addressLine1) {
        this.addressLine1 = addressLine1;
    }

    public String getAddressLine2() {
        return addressLine2;
    }

    public void setAddressLine2(String addressLine2) {
        this.addressLine2 = addressLine2;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getZip() {
        return zip;
    }

    public void setZip(String zip) {
        this.zip = zip;
    }

    public String getMailReceivable() {
        return mailReceivable;
    }

    public void setMailReceivable(String mailReceivable) {
        this.mailReceivable = mailReceivable;
    }

    public String getHomePhone() {
        return homePhone;
    }

    public void setHomePhone(String homePhone) {
        this.homePhone = homePhone;
    }

    public String getMobilePhone() {
        return mobilePhone;
    }

    public void setMobilePhone(String mobilePhone) {
        this.mobilePhone = mobilePhone;
    }
}
